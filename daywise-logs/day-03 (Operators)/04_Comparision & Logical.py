# Comparison Operator is used for Comparing two Values. The Output of it is always (Yes or No) OR (True or False)

a = 10
b = 20
print(a < b)

print(a == b)  # Equal to
print(a != b)  # Is not Equal to


# Logical Operator :
"""
Not Operator reverse the result output
"""

physics = 78
chemistry = 18

print(physics > 33 and chemistry > 33)
print(physics > 33 or chemistry > 33)
print(not (physics > 33 and chemistry > 33))
