# Operators : Operators are used to perform operation on variables or values

"""
Types of Operator :
1.Arithmetic Operator : +,-,*,/,%(Modulus),**(Exponentiation),//(Floor Division)
                        % = It is used to get reminder 5%3=2; 5%10=5 [Note : If the first value is lower than the second value then the output is first value]
                        ** = It is used for Power a number. 5**3 = 5*5*5 =125
                        // = It gives the output as Integer. It didn't roundoff but gets lower integer as output. 10//4=2 and -10//4=-3 (-3 is lower than -2.5)
2.Assignment Operator :
3.Comparision Operator :
4:Logical Operator :
5.Identity Operator :
6.Membership Operator :
7.Bitwise Operator :
"""

a = 10
b = 4

print(a + b)
print(a - b)
print(a * b)
print(a / b)  # gives output as float
print(a % b)
print(a**b)
print(a // b)  # gives output as integer
